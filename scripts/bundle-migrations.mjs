#!/usr/bin/env node
// Bundles all supabase/migrations/*.sql into a single TS module
// that the offline app loads at boot and applies in order.
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const migrationsDir = join(here, "..", "supabase", "migrations");
const outFile = join(here, "..", "src", "lib", "local-migrations.generated.ts");

const files = readdirSync(migrationsDir).filter((f) => f.endsWith(".sql")).sort();

const entries = files.map((name) => {
  const sql = readFileSync(join(migrationsDir, name), "utf8");
  // Escape backticks and ${} so we can embed safely in a template literal
  const escaped = sql.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
  return `  { name: ${JSON.stringify(name)}, sql: \`${escaped}\` },`;
}).join("\n");

const out = `// AUTO-GENERATED by scripts/bundle-migrations.mjs — do not edit by hand.
// Run \`node scripts/bundle-migrations.mjs\` whenever migrations change.
export type Migration = { name: string; sql: string };
export const MIGRATIONS: Migration[] = [
${entries}
];
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, out);
console.log(`[bundle-migrations] wrote ${files.length} migrations → ${outFile}`);
